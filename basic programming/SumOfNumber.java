//WAP to find the sum of all the number in the range 1 - 5
package jsp;

public class SumOfNumber 
{
	public static void main(String[] args)
	{
		int sum = 0;//3 6 10 15
		for(int i=1;i<=5;i++)
		{
			sum = sum +i;   //0+1=1  1+2=3 
		}
		System.out.println(sum);
	}
}

